package cn.jx.ecjtu.gw.model;

public class TypeModel {
    private String type;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public String toString() {
        return "TypeModel{" +
                "type='" + type + '\'' +
                '}';
    }
}